from datetime import datetime

current_date = datetime.now()

print(current_date)         # 2023-08-29 19:57:15.095842

print(current_date.date())  # 2023-08-29
print(current_date.time())  # 19:58:29.342332

print(current_date.year)    # 2023
print(current_date.month)   # 8
print(current_date.day)     # 29
print(current_date.hour)    # 19
print(current_date.minute)  # 59






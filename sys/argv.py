# Коли запускаємо збережений у файлі Python скрипт, є можливість 
# передати йому при запуску якісь аргументи, як у функцію. 
# Тоді наш скрипт може ці аргументи прийняти і якось змінити 
# свою поведінку. Зробити це можна за допомогою пакета sys, 
# в якому є список argv, де з'являються всі аргументи, з якими 
# був запущений скрипт.

import sys

for arg in sys.argv:
    print(arg)

